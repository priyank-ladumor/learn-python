# virtual env is a folder which contains all the dependencies of the project and not in your system

# pip install virtualenv

# how to create a virtual env
# python -m venv env
# or
# virtualenv env

# how to activate a virtual env
# source env/Scripts/activate (git bash)

# how to deactivate a virtual env
# deactivate

# how to remove a virtual env
# rm -rf env

# how to install a package in a virtual env
# pip install any-package

# check if a package is installed in a virtual env
# pip freeze
# pip list

# how to remove a package from a virtual env
# pip uninstall any-package

# how to upgrade a package in a virtual env
# pip install --upgrade any-package

# how to check this is a virtual env
# pip show virtualenv
# which python

# how to remove all the packages in a virtual env
# pip uninstall --all

# how to upgrade all the packages in a virtual env
# pip install --upgrade --all



# | Task                  | Recommended Command           |
# | --------------------- | ----------------------------- |
# | Create virtual env    | `python -m venv env`          |
# | Activate (CMD)        | `env\Scripts\activate`        |
# | Activate (PowerShell) | `.\env\Scripts\Activate.ps1`  |
# | Activate (Git Bash)   | `source env/Scripts/activate` |
# | Deactivate            | `deactivate`                  |
